    <h3 id="preface">Preface</h3>

    <ul>
        <li><strong>TOKI OSN Open Contest 2019</strong> is an open competition mirroring the National Science Olympiad in Informatics 2019 in Indonesia (<strong>OSN Informatika 2019</strong> in Bahasa).</li>
        <li>OSN Informatika is a national olympiad in informatics for high school students. It is one of the stages required for Indonesian students to represent Indonesia in IOI (International Olympiad in Informatics)</li>
        <li>The problemset is the same as the problemset in OSN Informatika 2019 and will be given in Bahasa Indonesia and English.</li>
        <li>The contest will be held on <a href="https://tlx.toki.id/contests">TLX (TOKI Online Judge)</a> (you may need to register for a TLX account if you do not have one).</li>
        <li>If you have any questions please contact <a href="mailto:sc-osn-2019@googlegroups.com">sc-osn-2019@googlegroups.com</a>.</li>
    </ul>

    <hr />

    <h3 id="rules-overview">Rules Overview</h3>

    <ul>
        <li>There will be <strong>one trial session</strong> and <strong>2 (two) competition days</strong>.</li>
        <li>Result of the trial session does <strong>not</strong> affect scoring.</li>
        <li>There will be <strong>3 (three) problems</strong> on each day to be solved in <strong>5 (five) hours</strong>.</li>
        <li>Each contestant may submit up to <strong>50 submissions</strong> for each problem.</li>
        <li>The supported programming languages are <strong>C, C++, and Pascal</strong>.</li>
        <li>During the competition, each contestant can only see their own scores.</li>
    </ul>

