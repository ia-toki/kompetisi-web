    <h3 id="preface">Preface</h3>

    <ul>
        <li><strong>TOKI OSN Open Contest 2018</strong> is an open competition mirroring the National Science Olympiad in Informatics 2018 in Indonesia (<strong>OSN Informatika 2018</strong> in Bahasa Indonesia).</li>
        <li>OSN Informatika is a national olympiad in informatics for high school students. It is the one of the stages required for Indonesian students to represent Indonesia in IOI (International Olympiad in Informatics)</li>
        <li>The problemset is the same as the problemset in OSN Informatika 2018, and will be given in Bahasa Indonesia and English.</li>
        <li>The contest will be held on <a href="https://tlx.toki.id/competition">TLX (TOKI Online Judge)</a> (you may need to register for a TLX account if you do not have one already).</li>
        <li>If you have any questions please contact <a href="mailto:scientific@toki.or.id">scientific@toki.or.id</a>.</li>
    </ul>

    <hr />

    <h3 id="rules-overview">Rules Overview</h3>

    <ul>
        <li>There will be one trial session and 2 (two) competition days.</li>
        <li>There will be 3 (three) problems on each day to be solved in 5 (five) hours.</li>
        <li>Each contestant may submit up to 50 solutions for each problem.</li>
        <li>The supported programming languages are C, C++, and Pascal.</li>
        <li>During the competition, each contestant can only see their own scores.</li>
    </ul>

