    <h3 id="problems-types">Problems Types</h3>

    <ul>
        <li>There are 3 possible problem types.</li>
        <li><strong>Batch</strong> problems:
            <ul>
                <li>The most common problem type.</li>
                <li>Given input, contestants write a program that reads input and writes output satisfying the problem description.</li>
                <li>The program must run within the given time and memory limits.</li>
            </ul>
        </li>
        <li><strong>Interactive</strong> problems:
            <ul>
                <li>Contestants write a program that interacts with judge's program.</li>
                <li>Judge's program writes to stdout and is given as the input for contestant's program.</li>
                <li>Contestant's program writes to stdout and is given as the input for judge's program.</li>
                <li>The intreaction continues until a given goal is reached.</li>
                <li>Contestant's program must output answer within the given time and memory limits.</li>
            </ul>
        </li>
        <li><strong>Output-only</strong> problems:
            <ul>
                <li>Contestants are be given a set of input test cases.</li>
                <li>Contestants submit output for each input test case.</li>
                <li>Contestants do not have to write a program; the outputs may be solved manually.</li>
            </ul>
        </li>
    </ul>

    <hr />

    <h3 id="scoring">Scoring</h3>

    <ul>
        <li>There are two types of scoring: standard and creative.</li>
        <li>Creative problems will be stated explicitly in problem description.</li>
        <li><strong>Standard</strong> scoring:
            <ul>
                <li>For batch and interactive problems:
                    <ul>
                        <li>A problem consists of multiple subtasks with various points.</li>
                        <li>A subtask consists of multiple test cases.</li>
                        <li>A submission gets a subtask's points if it solves all of its test cases.</li>
                        <li>A problem may have open subtasks where the test cases are given to contestants.</li>
                    </ul>
                </li>
                <li>For output-only problems:
                    <ul>
                        <li>A problem consists of multiple test cases with various points.</li>
                        <li>A submission gets the test case's points if it produces the correct output.</li>
                    </ul>
                </li>
            </ul>
        </li>
        <li><strong>Creative</strong> scoring:
            <ul>
                <li>The scoring formula will vary for each problem and will be explicitly stated in problem description.</li>
            </ul>
        </li>
    </ul>

    <hr />

    <h3 id="grading-system">Grading System</h3>

    <ul>
        <li>Source code limit for each submission is 300 KB.</li>
        <li>For each test case, the grader will output one of:
            <ul>
                <li>AC (Accepted): program solved the problem within the given time and memory limits.</li>
                <li>WA (Wrong Answer): program stopped within given time and memory limits, but produced the wrong output.</li>
                <li>RTE (Runtime Error): program crashed or exceedeed the memory limit.</li>
                <li>TLE (Time Limit Exceeded): program exceeded the time limit.</li>
                <li>Skipped: test case is not graded because there was a test case in the same subtask that was not solved.</li>
            </ul>
        </li>
    </ul>
