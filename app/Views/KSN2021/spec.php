<h3 id="perangkat-yang-harus-disiapkan-oleh-peserta">Perangkat yang harus disiapkan oleh peserta</h3>

<ol>
  <li>Sebuah laptop (diutamakan) atau PC yang terkoneksi dengan internet untuk mengerjakan soal-soal kompetisi.</li>
  <li>Sebuah <em>smartphone</em> (diutamakan), laptop, atau webcam dan dilengkapi mikrofon yang terkoneksi dengan internet untuk merekam aktvitas peserta selama lomba dan terhubung ke Zoom.</li>
  <li>Kedua perangkat tersebut perlu dilengkapi dengan kuota internet sehingga mampu terhubung secara terus-menerus selama pelaksanaan kompetisi.</li>
  <li>Perangkat untuk Zoom adalah perangkat yang berbeda dengan perangkat untuk mengerjakan soal.</li>
</ol>

